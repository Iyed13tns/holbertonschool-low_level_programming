#include "main.h"

/**
 * main - Prints the name of the program followed by a newline.
 * @argv: Argument vector (array of arguments).
 * Return: Always 0.
 */
int main(int argc, char *argv[])
{
    /* Print the program's name which is stored in argv[0] */
    printf("%s\n", argv[0]);
    return (0);
}
